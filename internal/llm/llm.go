// Close cleans up resources used by the client
func (c *Client) Close() {
	if c.gClient != nil {
		if err := c.gClient.Close(); err != nil {
			// Log error but don't return it since Close() should be idempotent
			fmt.Printf("Warning: failed to close LLM client: %s\n", err)
		}
	}
}

